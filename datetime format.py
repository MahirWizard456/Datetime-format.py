#Time Table
import datetime
x=datetime.datetime.now()
print(x)

#day
print(x.strftime("%a"))
print(x.strftime("%A"))

#Date
print(x.strftime("%d"))

#Month
print(x.strftime("%B"))
print(x.strftime("%b"))
print(x.strftime("%m"))

#Year
print(x.strftime("%Y"))
print(x.strftime("%y"))

#Time
print(x.strftime("%H"))
print(x.strftime("%I"))
print(x.strftime("%p"))
print(x.strftime("%M"))
print(x.strftime("%S"))






